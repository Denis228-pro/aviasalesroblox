    def _format_status(self, status: str, stats: Dict[str, Any] = None) -> str:
        """–§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Å—Ç–∞—Ç—É—Å–∞ —Å –ø–æ–¥—Å—Ç–∞–Ω–æ–≤–∫–æ–π —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏"""
        if not stats:
            return status

        try:
            formatted = status
            for key, value in stats.items():
                placeholder = f"{{{key}}}"
                if placeholder in formatted:
                    formatted = formatted.replace(placeholder, str(value))

            # –û–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ –¥–ª–∏–Ω—ã –¥–ª—è Discord
            if len(formatted) > 128:
                formatted = formatted[:125] + "..."

            return formatted
        except Exception:
            return status

